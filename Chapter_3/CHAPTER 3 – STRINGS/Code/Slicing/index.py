# ! Positive Slicinng
name = "muhammadRaza"
shortName = name[0:8]
print(shortName)
# ! Negative Slicing
name2 = 'muhammadRaza'
shortName2 = name2[-12:-4]
print(shortName2)
# ! Skip Vlaue Slicing
a = "0123456789"
print(a[1:6:2])
word = "amazing"
print(word[:7]) # same as word[0:7]
print(word[3:]) # same as word[3:8]